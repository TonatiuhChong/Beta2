package com.example.hombr.beta.Adapters;

public class ListItemAcciones {
    private String desc;
    private int Foto;

    public ListItemAcciones(String desc, int foto) {
        this.desc = desc;
        this.Foto = foto;
    }

    public String getDesc() {
        return desc;
    }

    public int getFoto() {
        return Foto;
    }
}

